-- DDL Syntax:
-- Create 'credit_card' schema
CREATE SCHEMA credit_card;

-- Create 'clients' table
CREATE TABLE credit_card.clients (
  client_id     INT PRIMARY KEY,
  first_name    VARCHAR(50) NOT NULL,
  last_name     VARCHAR(50) NOT NULL,
  email         VARCHAR(100) UNIQUE NOT NULL,
  phone         VARCHAR(20),
  date_of_birth DATE,
  join_date     DATE NOT NULL
);

-- Create 'credit_cards' table
CREATE TABLE credit_card.credit_cards (
  card_id       INT PRIMARY KEY,
  client_id     INT NOT NULL,
  card_number   VARCHAR(20) NOT NULL,
  card_type     VARCHAR(20) NOT NULL,
  issue_date    DATE NOT NULL,
  expiry_date   DATE NOT NULL,
  credit_limit  DECIMAL(12,2) NOT NULL,
  status        VARCHAR(20) NOT NULL,
  FOREIGN KEY (client_id) REFERENCES credit_card.clients(client_id)
);

-- Create 'card_transactions' table
CREATE TABLE credit_card.card_transactions (
  txn_id        INT PRIMARY KEY,
  card_id       INT NOT NULL,
  txn_date      DATE       NOT NULL,
  merchant      VARCHAR(100),
  amount        DECIMAL(12,2) NOT NULL,
  currency      CHAR(3)    NOT NULL,
  category      VARCHAR(50),
  FOREIGN KEY (card_id) REFERENCES credit_card.credit_cards(card_id)
);

-- Create 'statements' table
CREATE TABLE credit_card.statements (
  statement_id     INT PRIMARY KEY,
  card_id          INT NOT NULL,
  statement_date   DATE       NOT NULL,
  due_date         DATE       NOT NULL,
  opening_balance  DECIMAL(12,2) NOT NULL,
  closing_balance  DECIMAL(12,2) NOT NULL,
  minimum_due      DECIMAL(12,2) NOT NULL,
  FOREIGN KEY (card_id) REFERENCES credit_card.credit_cards(card_id)
);

-- Create 'payments' table
CREATE TABLE credit_card.payments (
  payment_id      INT PRIMARY KEY,
  statement_id    INT NOT NULL,
  payment_date    DATE       NOT NULL,
  amount          DECIMAL(12,2) NOT NULL,
  payment_method  VARCHAR(20),
  FOREIGN KEY (statement_id) REFERENCES credit_card.statements(statement_id)
);



